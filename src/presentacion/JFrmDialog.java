    private void btn_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_insertActionPerformed
        // TODO add your handling code here: 
        
        this.cuenta.ingresarDinero(Double.parseDouble(txtfld_monto.getText())); 
        JOptionPane.showMessageDialog(null, "Se ha Ingresado Dinero Correctamente!"+"\nHa Agregado: "+txtfld_monto.getText()+"$ A la cuenta" 
                +"\nCuenta Nº:"+cuenta.getNumeroCuenta()+"\nTitular : "+cuenta.getNombreTitular()); 
        
    }//GEN-LAST:event_btn_insertActionPerformed

    private void btn_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmActionPerformed
        // TODO add your handling code here: 
       
        btn_insert.setEnabled(true); 
        btn_consultar.setEnabled(true);
        btn_retirar.setEnabled(true);  
        cuenta.setNombreTitular(txtfld_titular.getText());
        cuenta.setNumeroCuenta(Integer.parseInt(txtfld_numCuenta.getText())); 
        cuenta.setSaldo(Double.parseDouble(txtfld_monto.getText())); 
        
        label_numCuenta.setText("Nº:"+String.valueOf(cuenta.getNumeroCuenta())); 
        label_titular.setText("Titular:"+cuenta.getNombreTitular());  
        
        txtfld_titular.setEnabled(false); 
        txtfld_numCuenta.setEnabled(false); 
        btn_confirm.setEnabled(false); 
        
        JOptionPane.showMessageDialog(null, "Se ha registrado Correctamente!");
    }//GEN-LAST:event_btn_confirmActionPerformed

