    private void btn_retirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_retirarActionPerformed
        if(Double.parseDouble(txtfld_monto.getText())>cuenta.getSaldo()){
            JOptionPane.showMessageDialog(null, "Error, No cuenta con saldo Suficiente","Error Monto",JOptionPane.ERROR_MESSAGE);
        }
        else{
            cuenta.retirarDinero(Double.parseDouble(txtfld_monto.getText())); 
            JOptionPane.showMessageDialog(null, "Se ha retirado dinero Correctamente!");
            JOptionPane.showMessageDialog(null, "Se ha retirado Dinero Correctamente!"+"\nHa Retirado: "+txtfld_monto.getText()+"$ A la cuenta" 
                    +"\nCuenta Nº:"+cuenta.getNumeroCuenta() +"\nTitular : "+cuenta.getNombreTitular()); 
        }
        
    }//GEN-LAST:event_btn_retirarActionPerformed

